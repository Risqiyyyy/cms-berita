                        <p>Kemerdekaan berpendapat, kemerdekaan berekspresi, dan kemerdekaan pers adalah hak asasi
                            manusia yang dilindungi Pancasila, Undang-Undang Dasar 1945, dan Deklarasi Universal Hak
                            Asasi Manusia PBB. Keberadaan media siber di Indonesia juga merupakan bagian dari
                            kemerdekaan berpendapat, kemerdekaan berekspresi, dan kemerdekaan pers.</p>
                        <p>Media siber memiliki karakter khusus sehingga memerlukan pedoman agar pengelolaannya dapat
                            dilaksanakan secara profesional, memenuhi fungsi, hak, dan kewajibannya sesuai Undang-Undang
                            Nomor 40 Tahun 1999 tentang Pers dan Kode Etik Jurnalistik. Untuk itu Dewan Pers bersama
                            organisasi pers, pengelola media siber, dan masyarakat menyusun Pedoman Pemberitaan Media
                            Siber sebagai berikut:</p>
                        <p><strong>1.Â Ruang Lingkup</strong></p>
                        <p>a) Media Siber adalah segala bentuk media yang menggunakan wahana internet dan melaksanakan
                            kegiatan jurnalistik, serta memenuhi persyaratan Undang-Undang Pers dan Standar Perusahaan
                            Pers yang ditetapkan Dewan</p>
                        <p>b) Isi Buatan Pengguna (<em>User Generated Content</em>) adalah segala isi yang dibuat dan
                            atau dipublikasikan oleh pengguna media siber, antara lain, artikel, gambar, komentar,
                            suara, video dan berbagai bentuk unggahan yang melekat pada media siber, seperti blog,
                            forum, komentar pembaca atau pemirsa, dan bentuk</p>
                        <p><strong>2.Â Verifikasi dan keberimbangan berita</strong></p>
                        <p>a) Pada prinsipnya setiap berita harus melalui verifikasi.</p>
                        <p>b) Berita yang dapat merugikan pihak lain memerlukan verifikasi pada berita yang sama untuk
                            memenuhi prinsip akurasi dan</p>
                        <p>c) Ketentuan dalam butir (a) di atas dikecualikan, dengan syarat:</p>
                        <ul>
                            <li>
                                <ul>
                                    <li>
                                        <ul>
                                            <li>Berita benar-benar mengandung kepentingan publik yang bersifat mendesak;
                                            </li>
                                            <li>Sumber berita yang pertama adalah sumber yang jelas disebutkan
                                                identitasnya, kredibel dan kompeten;</li>
                                            <li>Subyek berita yang harus dikonfirmasi tidak diketahui keberadaannya dan
                                                atau tidak dapat diwawancarai;</li>
                                            <li>Media memberikan penjelasan kepada pembaca bahwa berita tersebut masih
                                                memerlukan verifikasi lebih lanjut yang diupayakan dalam waktu.
                                                Penjelasan dimuat pada bagian akhir dari berita yang sama, di dalam
                                                kurung dan menggunakan huruf miring.</li>
                                        </ul>
                                    </li>
                                </ul>
                            </li>
                        </ul>
                        <p>d) Setelah memuat berita sesuai dengan butir (c), media wajib meneruskan upaya verifikasi,
                            dan setelah verifikasi didapatkan, hasil verifikasi dicantumkan pada berita pemutakhiran
                            (<em>update</em>) dengan tautan pada berita yang belum</p>
                        <ol start="3">
                            <li><strong>Isi Buatan Pengguna (<em>User Generated Content</em>)</strong></li>
                        </ol>
                        <p>a) Media siber wajib mencantumkan syarat dan ketentuan mengenai Isi Buatan Pengguna yang
                            tidak bertentangan dengan Undang-Undang No. 40 tahun 1999 tentang Pers dan Kode Etik
                            Jurnalistik, yang ditempatkan secara terang dan jelas.</p>
                        <p>b) Media siber mewajibkan setiap pengguna untuk melakukan registrasi keanggotaan dan
                            melakukan prosesÂ <em>log-inÂ </em>terlebih dahulu untuk dapat mempublikasikan semua bentuk
                            Isi Buatan Ketentuan mengenaiÂ <em>log-inÂ </em>akan diatur lebih lanjut.</p>
                        <p>c) Dalam registrasi tersebut, media siber mewajibkan pengguna memberi persetujuan tertulis
                            bahwa Isi Buatan Pengguna yang dipublikasikan:</p>
                        <ul>
                            <li>
                                <ul>
                                    <li>
                                        <ul>
                                            <li>Tidak memuat isi bohong, fitnah, sadis dan cabul;</li>
                                            <li>Tidak memuat isi yang mengandung prasangka dan kebencian terkait dengan
                                                suku, agama, ras, dan antargolongan (SARA), serta menganjurkan tindakan
                                                kekerasan;</li>
                                            <li>Tidak memuat isi diskriminatif atas dasar perbedaan jenis kelamin dan
                                                bahasa, serta tidak merendahkan martabat orang lemah, miskin, sakit,
                                                cacat jiwa, atau cacat</li>
                                        </ul>
                                    </li>
                                </ul>
                            </li>
                        </ul>
                        <p>d) Media siber memiliki kewenangan mutlak untuk mengedit atau menghapus Isi Buatan Pengguna
                            yang bertentangan dengan butir (c).</p>
                        <p>e) Media siber wajib menyediakan mekanisme pengaduan Isi Buatan Pengguna yang dinilai
                            melanggar ketentuan pada butir (c). Mekanisme tersebut harus disediakan di tempat yang
                            dengan mudah dapat diakses</p>
                        <p>f) Media siber wajib menyunting, menghapus, dan melakukan tindakan koreksi setiap Isi Buatan
                            Pengguna yang dilaporkan dan melanggar ketentuan butir (c), sesegera mungkin secara
                            proporsional selambat-lambatnya 2 x 24 jam setelah pengaduan</p>
                        <p>g) Media siber yang telah memenuhi ketentuan pada butir (a), (b), (c), dan (f) tidak dibebani
                            tanggung jawab atas masalah yang ditimbulkan akibat pemuatan isi yang melanggar ketentuan
                            pada butir (c).</p>
                        <p>h) Media siber bertanggung jawab atas Isi Buatan Pengguna yang dilaporkan bila tidak
                            mengambil tindakan koreksi setelah batas waktu sebagaimana tersebut pada butir (f).</p>
                        <p><strong>4.Â Ralat, Koreksi, dan Hak Jawab</strong></p>
                        <p>q) Ralat, koreksi, dan hak jawab mengacu pada Undang-Undang Pers, Kode Etik Jurnalistik, dan
                            Pedoman Hak Jawab yang ditetapkan Dewan</p>
                        <p>b) Ralat, koreksi dan atau hak jawab wajib ditautkan pada berita yang diralat, dikoreksi atau
                            yang diberi hak</p>
                        <p>c) Di setiap berita ralat, koreksi, dan hak jawab wajib dicantumkan waktu pemuatan ralat,
                            koreksi, dan atau hak jawab</p>
                        <p>d) Bila suatu berita media siber tertentu disebarluaskan media siber lain, maka:</p>
                        <ul>
                            <li>
                                <ul>
                                    <li>
                                        <ul>
                                            <li>Tanggung jawab media siber pembuat berita terbatas pada berita yang
                                                dipublikasikan di media siber tersebut atau media siber yang berada di
                                                bawah otoritas teknisnya;</li>
                                            <li>Koreksi berita yang dilakukan oleh sebuah media siber, juga harus
                                                dilakukan oleh media siber lain yang mengutip berita dari media siber
                                                yang dikoreksi itu;</li>
                                            <li>Media yang menyebarluaskan berita dari sebuah media siber dan tidak
                                                melakukan koreksi atas berita sesuai yang dilakukan oleh media siber
                                                pemilik dan atau pembuat berita tersebut, bertanggung jawab penuh atas
                                                semua akibat hukum dari berita yang tidak dikoreksinya itu.</li>
                                        </ul>
                                    </li>
                                </ul>
                            </li>
                        </ul>
                        <p>e) Sesuai dengan Undang-Undang Pers, media siber yang tidak melayani hak jawab dapat dijatuhi
                            sanksi hukum pidana denda paling banyak Rp500.000.000 (Lima ratus juta rupiah).</p>
                        <p><strong>5.Â Pencabutan Berita</strong></p>
                        <p>a) Berita yang sudah dipublikasikan tidak dapat dicabut karena alasan penyensoran dari pihak
                            luar redaksi, kecuali terkait masalah SARA, kesusilaan, masa depan anak, pengalaman
                            traumatik korban atau berdasarkan pertimbangan khusus lain yang ditetapkan Dewan</p>
                        <p>b) Media siber lain wajib mengikuti pencabutan kutipan berita dari media asal yang telah</p>
                        <p>c) Pencabutan berita wajib disertai dengan alasan pencabutan dan diumumkan kepada</p>
                        <p><strong>6.Â Iklan</strong></p>
                        <p>a) Media siber wajib membedakan dengan tegas antara produk berita dan iklan.</p>
